package com.divergent.assignment5;

/**
 * Default Value of Variable
 * 
 * @author Divergent
 *
 */
public class DefaultFieldClass {

	int a;
	double b;
	boolean c;
	String str;

	public static void main(String[] args) {

		DefaultFieldClass defaultf = new DefaultFieldClass();
		System.out.println(defaultf.a);
		System.out.println(defaultf.b);
		System.out.println(defaultf.c);
		System.out.println(defaultf.str);
	}

}
